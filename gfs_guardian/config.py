"""
全局配置：区域定义、调度间隔、并发限制、重试策略。
修改此文件即可调整所有行为参数，无需改动业务逻辑。
"""

from dataclasses import dataclass
from pathlib import Path

# 项目根目录（wx/）
ROOT: Path = Path(__file__).resolve().parents[1]


# ---------------------------------------------------------------------------
# 保障区域
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class Region:
    name:       str    # 唯一标识，用于日志和锁 key
    bbox:       str    # "W,S,E,N"（传给 --bbox）
    out_dir:    str    # 相对于 ROOT 的输出目录
    start_date: str    # 历史窗口起始日期 "YYYY-MM-DD"


REGIONS: list[Region] = [
    Region("china", "73,18,135,54", "data", "2025-09-01"),
]


# ---------------------------------------------------------------------------
# 巡检间隔
# ---------------------------------------------------------------------------

HIST_INTERVAL_SEC: int = 10 * 60    # 历史窗口巡检：每 10 分钟
RT_INTERVAL_SEC:   int =  5 * 60    # 实时窗口巡检：每 5 分钟


# ---------------------------------------------------------------------------
# 下载参数
# ---------------------------------------------------------------------------

HIST_CYCLE:   str = "00"
HIST_FH_SPEC: str = "0:23"          # 历史窗口 fh 范围
HIST_FH_START: int = 0
HIST_FH_END:   int = 23

RT_FH_SPEC:   str = "0:120"         # 实时窗口 fh 范围
RT_FH_START:  int = 0
RT_FH_END:    int = 120


# ---------------------------------------------------------------------------
# 并发与重试
# ---------------------------------------------------------------------------

MAX_CONCURRENT: int = 3             # 全局最大并发下载数

MAX_RETRIES:    int = 3             # 每个任务最多重试次数
# 第 n 次重试前等待的秒数（0-indexed）
RETRY_BACKOFF_SEC: list[int] = [60, 300, 900]   # 1 min, 5 min, 15 min


# ---------------------------------------------------------------------------
# 日志
# ---------------------------------------------------------------------------

LOG_DIR:          Path = ROOT / "logs"
LOG_FILE:         Path = LOG_DIR / "gfs_guardian.log"
TASK_LOG_DIR:     Path = LOG_DIR / "tasks"
LOG_MAX_BYTES:    int  = 50 * 1024 * 1024   # 50 MB per file
LOG_BACKUP_COUNT: int  = 5
